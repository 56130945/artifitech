<!-- Add JavaScript for product section switching -->
<script>
document.addEventListener('DOMContentLoaded', function() {
    const productLinks = document.querySelectorAll('.product-nav a[data-product]');
    const productSections = document.querySelectorAll('.product-section');

    productLinks.forEach(link => {
        link.addEventListener('click', function(e) {
            e.preventDefault();
            
            // Remove active class from all links and sections
            productLinks.forEach(l => l.classList.remove('active'));
            productSections.forEach(s => s.classList.remove('active'));
            
            // Add active class to clicked link
            this.classList.add('active');
            
            // Show corresponding section
            const targetId = this.getAttribute('data-product');
            document.getElementById(targetId).classList.add('active');
        });
    });
});
</script>
